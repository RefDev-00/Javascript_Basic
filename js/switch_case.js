// Switch Case sama seperti javascript hanya saja bisa digunakan dalam kasus tertentu
let warna = prompt("Input Warna Anda ?");

switch (warna) {
  case "Merah":
    warna = alert("Warna anda Merah");
    break;
  case "Kuning":
    warna = alert("Warna anda Kuning");
    break;
  case "Hijau":
    warna = alert("Warna anda Hijau");
    break;
  case "Biru":
    warna = alert("Warna anda Biru");
    break;
  default:
    warna = alert("Tidak dimengerti");
}
